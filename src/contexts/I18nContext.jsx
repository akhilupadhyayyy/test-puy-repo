export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("xy_lang") || "zh");

  useEffect(() => {
    localStorage.setItem("xy_lang", lang);
    document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  }, [lang]);

  const value = useMemo(() => {
    const t = (key) => DICT[lang]?.[key] ?? DICT.zh[key] ?? key;
    return { lang, setLang, t };
  }, [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (ctx) return ctx;
  // Fallback when used outside provider — prevents crashes.
  return {
    lang: "zh",
    setLang: () => {},
    t: (key) => DICT.zh[key] ?? key,
  };
};
